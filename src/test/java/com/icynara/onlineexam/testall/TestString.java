public class TestString {
	public static void main(String[] args) {
		String str = "[2, A, A, A, A,B,C, t, A,B,C,D]";
		str = str.substring(4,str.length()-1);
		String[] s = str.split(" ");
		String ss = "";
		for(String string : s){
			ss+="["+string.replaceAll(",", "")+"]";
		}
		System.out.println(ss);
	}
}
